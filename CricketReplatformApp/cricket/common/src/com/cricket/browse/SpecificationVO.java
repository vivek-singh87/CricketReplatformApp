package com.cricket.browse;

public class SpecificationVO {
	
	private String specificationName;
	private String specificationvalue;

	/**
	 * @return the specificationvalue
	 */
	public String getSpecificationvalue() {
		return specificationvalue;
	}

	/**
	 * @param pSpecificationvalue the specificationvalue to set
	 */
	public void setSpecificationvalue(String pSpecificationvalue) {
		specificationvalue = pSpecificationvalue;
	}

	public String getSpecificationName() {
		return specificationName;
	}

	public void setSpecificationName(String specificationName) {
		this.specificationName = specificationName;
	}
	
	
	
}
